function soma(item1, item2){
return item1 + item2;
}

function subtracao(item1, item2){
    return item1 - item2;
}

console.log('A soma é igual a:', soma(5,6));
console.log('A soma é igual a:', soma(54,62));
console.log('A subtração é igual a:', subtracao(54,62));

